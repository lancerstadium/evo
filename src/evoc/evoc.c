#include "evoc.h"

// ==================================================================================== //
//                                  Proc Entry: evoc
// ==================================================================================== //


int main(int argc, char **argv) {
    if(argc != 2) {
        log_error("usage: %s <stmt>", argv[0]);
        return 1;
    }

    // 解析用户输入
    Token *tok = evoc_tokenize(argv[1]);
    Func *prog = evoc_parse(tok);
    evoc_codegen(prog); 
    return 0;
}